int main() {
    // increment operator
    int a = 1;
    ++a;
    a++;

    // decrement operator
    int b = 2;
    b--;
    --b;

    // address-of operator
    int x = 5;
    int* y = &x;

    // dereference operator
    int z = *y;

    // bitwise not operator
    int u = 1;
    int v = ~u;

    // logical not operator
    int w = !v;
    
    // negate operator
    int m = -w;
}
